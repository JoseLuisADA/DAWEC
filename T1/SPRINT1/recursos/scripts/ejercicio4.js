let numero = prompt("Vamos a comprobar si funciona el comparador de mayor, menor e igual, teniendo como referencia el número 10. Introduce un número :");

if(numero > 10){
    alert("¡El número es mayor que 10!");
} else if(numero < 10){
    alert("¡El número es menor que 10!");
} else if(numero == 10){
    alert("¡Los numeros son iguales!");
}